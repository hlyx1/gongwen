/**
 * 获取所有章节的扁平化列表（用于目录导航）
 */
export function flattenSections(sections: Section[], level: number = 0): Array<Section & { level: number }> {
  const result: Array<Section & { level: number }> = []
  sections.forEach((section) => {
    result.push({ ...section, level })
    if (section.children) {
      result.push(...flattenSections(section.children, level + 1))
    }
  })
  return result
}

/**
 * 获取一级和二级章节（用于目录折叠）
 */
export function getTocSections(sections: Section[]): Array<Section & { hasChildren: boolean }> {
  return sections.map((section) => ({
    ...section,
    hasChildren: !!(section.children && section.children.length > 0),
  }))
}
